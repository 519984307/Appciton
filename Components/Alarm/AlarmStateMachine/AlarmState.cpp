/**************************************************************************************************
 * 启动定时器。
 *************************************************************************************************/
void AlarmState::beginTimer(int interval)
{
    if (0 == _timerID)
    {
        _timerID = startTimer(interval);
    }
}

/**************************************************************************************************
 * 关闭定时器。
 *************************************************************************************************/
void AlarmState::endTimer()
{
    if (0 != _timerID)
    {
        killTimer(_timerID);
        _timerID = 0;
    }
}

