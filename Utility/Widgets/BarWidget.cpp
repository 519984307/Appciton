#include <QStylePainter>
#include "BarWidget.h"

/**************************************************************************************************
 * 构造函数。
 *************************************************************************************************/
BarWidget::BarWidget(QWidget *parent)
    : QWidget(parent)
{

}

/**************************************************************************************************
 * 析构函数。
 *************************************************************************************************/
BarWidget::~BarWidget()
{
}

/**************************************************************************************************
 * 绘图函数。
 *************************************************************************************************/
void BarWidget::paintEvent(QPaintEvent *e)
{
    if (NULL == e)
    {
		return;
	}

    QPainter painter(this);
    painter.fillRect(rect(), palette().color(QPalette::WindowText));
}
